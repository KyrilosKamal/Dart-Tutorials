void main() {
  //Addition
  int a = 4;
  int b = 6;
  int c = a + b;
  print(c);

  //Subtraction
  int d = 4;
  int e = 6;
  int f = d - e;
  print(f);

  //Multiplication
  int g = 4;
  int h = 6;
  int i = g * h;
  print(i);

  //Division

  int j = 4;
  int k = 6;
  double l = j / k;
  print(l);

  /**
   * we can remove decimal number and get an Integer number even in Division process 
   * using '~' mark before the division mark 
   */
  int x = 12;
  int y = 7;
  int result = x ~/ y;
  print(result);

  //Modules
  double m = 6;
  double n = 4;
  double o = m % n;
  print(o);

  /**
   * There also another method called incremental like this:
   * o++; it's the same like o=o+1;.
   * and decremintal o--; it's the same as o=o-1;
   */
}
